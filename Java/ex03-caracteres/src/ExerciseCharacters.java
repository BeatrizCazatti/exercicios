public class ExerciseCharacters {
    public static void main(String[]args) {
        //char sempre declara valores entre aspas simples
        char letra = 'a';
        System.out.println(letra);

        char letra2 = 65;
        System.out.println(letra2);

        //entre aspas duplas
        String palavra = " Beatriz";
        System.out.println(palavra);
        System.out.println(letra2 + palavra);


    }
}